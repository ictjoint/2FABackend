'use strict';
//=============================================================================
/**
 * Module dependencies
 */
//=============================================================================
const
    mongoose = require('mongoose');
//=============================================================================
/**
 * RolesUsers Schema
 */
//=============================================================================
const OtpUsersSchema = mongoose.Schema({
    userid: {
        type: mongoose.Schema.ObjectId,
        required: true,
    },
    otpkey: {
        type: String,
        required: true
    },
    otpauth_url:{
    	type: String
    }
});
//=============================================================================
/**
 * Compile to Model
 */
//=============================================================================
const OtpUsersModel = mongoose.model('OtpUsers', OtpUsersSchema);
//=============================================================================
/**
 * Export RolesUsersModel
 */
//=============================================================================
module.exports = OtpUsersModel;
//=============================================================================
